package aula01_variaveis;

public class Curiosidadeint {
    public static void main(String[] args) {
        int var1 = 2147483647;
        int var2 = 2;
        long var3 = 2147483647L;
        long var4 = 2L;

        System.out.println("Var 1: " + var1);
        System.out.println("Var 2: " + var2);
        System.out.println("Var 1 + Var 2: " + (var1 + var2));

        System.out.println("Var 3: " + var3);
        System.out.println("Var 4: " + var4);
        System.out.println("Var 3 + Var 4: " + (var3 + var4));
    }
}
